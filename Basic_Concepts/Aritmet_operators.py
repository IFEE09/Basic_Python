import math

numberOne = 20 
numberTwo = 12
sumOfOperators = numberOne + numberTwo
print(sumOfOperators)
multOfOperators = numberOne * numberTwo
print(multOfOperators)
divOfOperators = numberOne / numberTwo
print(divOfOperators)
residualOfOperators = numberOne % numberTwo
print(residualOfOperators)
potentialOfOperators = numberOne**2 #this is "(first...)^2"
print(potentialOfOperators)
sqrtOfOperators = numberTwo**0.5 #this is "(first...)^(1/2) or sqrt"
print(sqrtOfOperators)
#Other way to make a sqrt
otherWaySqrt = math.sqrt(numberTwo)
print(otherWaySqrt)

